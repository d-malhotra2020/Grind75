class Solution:
    def numIslands(self, grid: List[List[str]]) -> int:
        if not grid:
          return 0
        rows = len(grid)
        columns = len(grid[0])
        islands = 0

        def dfs(r, c):
          if r < 0 or c < 0 or r >= rows or c >= columns:
            return 
          if grid[r][c] != "1":
            return
          grid[r][c] = "0"
          dfs(r+1, c)
          dfs(r-1, c)
          dfs(r, c+1)
          dfs(r, c-1)

        for r in range(rows):
          for c in range(columns):
            if grid[r][c] == "1":
              dfs(r, c)
              islands += 1
        return islands